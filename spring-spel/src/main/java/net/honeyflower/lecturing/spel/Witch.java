package net.honeyflower.lecturing.spel;


public class Witch {
   private String name;
   private boolean wicked;

   public void setName(String name) {
      this.name = name;
   }

   public String getName() {
      return name;
   }

   public void setWicked(boolean wicked) {
      this.wicked = wicked;
   }

   public boolean isWicked() {
      return wicked;
   }

}
